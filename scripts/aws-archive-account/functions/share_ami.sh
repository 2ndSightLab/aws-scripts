share_ami(){
  local AMI_ID="$1"
  local to_account="$2"
  local archive_from="$3"
  local region="$4"

  echo "Share ami: $AMI_ID"
  
  aws ec2 modify-image-attribute --image-id $AMI_ID --launch-permission "Add=[{UserId=$to_account}]" \
           --profile $archive_from --region $region --color off
  echo "Shared the AMI."
  echo "Status is pending...."
}
